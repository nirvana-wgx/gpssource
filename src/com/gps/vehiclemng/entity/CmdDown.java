package com.gps.vehiclemng.entity;

public class CmdDown {
	private String cd_id;
	private String sim;
	private String content;
	private String createtime;
	private String sendtime;
	private String state;
	private String numbers;
	
	public CmdDown(){}
	
	public CmdDown(String sim,String content,String createtime,
			String sendtime,String state,String numbers){
		this.sim = sim;
		this.content = content;
		this.createtime = createtime;
		this.sendtime = sendtime;
		this.state = state;
		this.numbers = numbers;
	}
	
	public CmdDown(String cd_id,String sim,String content,String createtime,
			String sendtime,String state,String numbers){
		this.cd_id = cd_id;
		this.sim = sim;
		this.content = content;
		this.createtime = createtime;
		this.sendtime = sendtime;
		this.state = state;
	}

	public String getCd_id() {
		return cd_id;
	}

	public void setCd_id(String cd_id) {
		this.cd_id = cd_id;
	}

	public String getSim() {
		return sim;
	}

	public void setSim(String sim) {
		this.sim = sim;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getCreatetime() {
		return createtime;
	}

	public void setCreatetime(String createtime) {
		this.createtime = createtime;
	}

	public String getSendtime() {
		return sendtime;
	}

	public void setSendtime(String sendtime) {
		this.sendtime = sendtime;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public String getNumbers() {
		return numbers;
	}

	public void setNumbers(String numbers) {
		this.numbers = numbers;
	}
}
