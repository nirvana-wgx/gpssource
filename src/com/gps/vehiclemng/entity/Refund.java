package com.gps.vehiclemng.entity;


 
public class Refund implements java.io.Serializable{

	/**
	 * 还款
	 */
	private static final long serialVersionUID = 1L;

	private String rid ;//主键
	private String frid ;//车辆表主键
	private String refundmoney;//还款金额
	private String refundtime ;//还款时间
	private String remainmoney;//剩余金额
	public String getRid() {
		return rid;
	}
	public void setRid(String rid) {
		this.rid = rid;
	}
	public String getFrid() {
		return frid;
	}
	public void setFrid(String frid) {
		this.frid = frid;
	}
	 
	public String getRefundtime() {
		return refundtime;
	}
	public void setRefundtime(String refundtime) {
		this.refundtime = refundtime;
	}
	public String getRefundmoney() {
		return refundmoney;
	}
	public void setRefundmoney(String refundmoney) {
		this.refundmoney = refundmoney;
	}
	public String getRemainmoney() {
		return remainmoney;
	}
	public void setRemainmoney(String remainmoney) {
		this.remainmoney = remainmoney;
	}
	 
}
